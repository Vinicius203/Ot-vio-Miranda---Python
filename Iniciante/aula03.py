# O python por ter tipagem dinâmica interpreta automaticamente o tipo da variável

print(1234) # variável inteira

# Ao usar aspas, as variáveis são interpretadas como strings
print('1234')
print("1234")

# Escape (NÃO UTILIZAR)
print("Luiz \"Otávio\"")

# Alternativa para o escape é utilizar aspas diferentes
print('"Vinicius"')